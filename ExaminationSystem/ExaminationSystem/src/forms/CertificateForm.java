    private void buttonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonDeleteActionPerformed
       CertificateBean bean = (CertificateBean)certificateList.getSelectedValue();
       if(bean!=null)
       try{
           DatabaseManager.deleteCertificate(bean.getCertificateId());
           refreshList();
           clear();
       }catch(Exception ex){
           ex.printStackTrace();
           JOptionPane.showMessageDialog(this,"Certificate isn't deleted...");
       }
    }//GEN-LAST:event_buttonDeleteActionPerformed

    private void certificateListValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_certificateListValueChanged
        CertificateBean bean = (CertificateBean)certificateList.getSelectedValue();
        if(bean == null )return;
        textFieldCertificateName.setText(bean.getCertificateName());
        textFieldCharges.setText(""+bean.getRate());
        textAreaRemarks.setText(bean.getRemarks());
    }//GEN-LAST:event_certificateListValueChanged

    private void buttonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAddActionPerformed
        String name = textFieldCertificateName.getText().trim();
        System.out.print(textFieldCharges.getText().trim());
        int rate = Integer.parseInt(textFieldCharges.getText().trim());
        String remarks = textAreaRemarks.getText().trim();
        try{
            DatabaseManager.addCertificate(name, rate, remarks);
            refreshList();
            clear();
        }catch(Exception ex){
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Record not added..."+ ex);
        }
    }//GEN-LAST:event_buttonAddActionPerformed

    private void buttonClearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonClearActionPerformed
        clear();
    }//GEN-LAST:event_buttonClearActionPerformed

    private void buttonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonUpdateActionPerformed
       CertificateBean bean = (CertificateBean)certificateList.getSelectedValue();
       String name = textFieldCertificateName.getText().trim();
       System.out.print(textFieldCharges.getText().trim());
       int rate = Integer.parseInt(textFieldCharges.getText().trim());
       String remarks = textAreaRemarks.getText().trim();
        
       if(bean!=null)
       try{
           DatabaseManager.updateCertificate(bean.getCertificateId(), name, rate, remarks);
           refreshList();
           clear();
       }catch(Exception ex){
           ex.printStackTrace();
           JOptionPane.showMessageDialog(this,"Certificate isn't deleted...");
       }
    }//GEN-LAST:event_buttonUpdateActionPerformed

