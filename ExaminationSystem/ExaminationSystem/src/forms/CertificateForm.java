    public void refreshList()throws Exception{
        ArrayList list = database.DatabaseManager.getAllCertificate();
        System.out.println(list.size());
        certificateList.setListData(list.toArray());
    }
    
    public void clear(){
        textFieldCertificateName.setText("");
        textFieldCharges.setText("");
        textAreaRemarks.setText("");
    }
    
    
